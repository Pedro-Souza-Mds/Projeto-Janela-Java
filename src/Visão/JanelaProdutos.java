    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // buscar:
        int i = Integer.parseInt(jTextField1.getText());
        ControleProduto con = new ControleProduto();
        Produto p = con.getPorId(i);
        if (p != null) {
            jTextField3.setText(p.getDescricao());
            jTextField2.setText(p.getValorUnit());
            int k = 0;
            while (itens[k].id != p.getFornecedor().getId()) {
                k++;
            }
            jComboBox1.setSelectedItem(itens[k]);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    
