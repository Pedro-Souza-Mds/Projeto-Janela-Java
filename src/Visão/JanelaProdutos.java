    public JanelaProdutos(java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        initComponents();

        ControleFornecedor cf = new ControleFornecedor();

        ArrayList cids = cf.getAll();
        itens = new ItemFornecedor[cids.size()];
        for (int i = 0; i < cids.size(); i++) {
            Fornecedor f = (Fornecedor) cids.get(i);
            itens[i] = new ItemFornecedor();
            itens[i].nome = f.getNome();
            itens[i].id = f.getId();
        }
        jComboBox1.setModel(new javax.swing.DefaultComboBoxModel(itens));

    }

