    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // buscar:
        jTextArea1.setText("");
        ControleProduto cntp = new ControleProduto();
        ItemFornecedor ifo = (ItemFornecedor) jComboBox1.getSelectedItem();
        ArrayList lista = cntp.getPorFornecedor(ifo.id);
        for (int i = 0; i < lista.size(); i++) {
            Produto pro = (Produto) lista.get(i);
            String detalhe = pro.getId() + "\t"
                    + pro.getDescricao() + "\t"
                    + pro.getValorUnit() + "\t ("
                    + pro.getFornecedor().getNome() + "-"
                    + pro.getFornecedor().getFone() + ") \n";
            jTextArea1.append(detalhe);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

